<template>
  <el-card shadow="never" class="border-0">
    <!-- 新增|刷新 -->
    <ListHeader layout="create,delete,refresh" @create="handleCreate" @refresh="getData" @delete="handleMultiDelete" />
    <el-table ref="multipleTableRef" @selection-change="handleSelectionChange" :data="tableData" stripe style="width: 100%" v-loading="loading">
      <el-table-column type="selection" width="55" />
      <el-table-column prop="name" label="规格名称" />
      <el-table-column prop="default" label="规格值" width="380" />
      <el-table-column prop="order" label="排序" />
      <el-table-column label="状态" width="120">
        <template #default="{ row }">
          <el-switch
            :modelValue="row.status"
            :active-value="1"
            :inactive-value="0"
            :loading="row.statusLoading"
            :disabled="row.super == 1"
            @change="handleStatusChange($event, row)"
          >
          </el-switch>
        </template>
      </el-table-column>
      <el-table-column label="操作" width="250" align="center">
        <template #default="scope">
          <el-button type="primary" size="small" text @click="handleEdit(scope.row)">修改</el-button>

          <el-popconfirm title="是否要删除该规格？" confirmButtonText="确认" cancelButtonText="取消" @confirm="handleDelete(scope.row.id)">
            <template #reference>
              <el-button text type="primary" size="small">删除</el-button>
            </template>
          </el-popconfirm>
        </template>
      </el-table-column>
    </el-table>

    <div class="flex items-center justify-center mt-5">
      <el-pagination background layout="prev, pager ,next" :total="total" :current-page="currentPage" :page-size="limit" @current-change="getData" />
    </div>

    <FormDrawer destroyOnClose ref="formDrawerRef" :title="drawerTitle" @submit="handleSubmit">
      <m-form-model :formItems="options.formItems" v-model="form" ref="formRef">
        <template #skuValue>
          <TagInput v-model="form.default" />
        </template>
      </m-form-model>
    </FormDrawer>
  </el-card>
</template>
<script setup>
import { computed, onMounted, ref, watch } from 'vue'
import ListHeader from '~/components/ListHeader.vue'
import FormDrawer from '~/components/FormDrawer.vue'
import TagInput from '~/components/TagInput.vue'
import { getSkusList, createSkus, updateSkus, deleteSkus, updateSkusStatus } from '~/api/skus'
import { toast } from '~/composables/util'

import { useInitTable, useInitForm } from '~/composables/useCommon.js'

const {
  tableData,
  loading,
  currentPage,
  total,
  limit,
  getData,
  handleDelete,
  handleStatusChange,
  handleSelectionChange,
  multipleTableRef,
  handleMultiDelete,
} = useInitTable({
  getList: getSkusList,
  delete: deleteSkus,
  updateStatus: updateSkusStatus,
})

const beforeSubmit = (data) => {
  data.default = form.default
  return data
}

const { formDrawerRef, formRef, form, rules, drawerTitle, handleSubmit, handleCreate, handleEdit } = useInitForm({
  form: {
    name: '',
    status: 1,
    default: '',
    order: 50,
  },
  rules: {
    name: [
      {
        required: true,
        message: '规格名称不能为空',
        trigger: 'blur',
      },
    ],
    default: [
      {
        required: true,
        message: '规格值不能为空',
        trigger: 'blur',
      },
    ],
  },
  component: 'm-form',
  getData,
  update: updateSkus,
  create: createSkus,
  // beforeSubmit,
})

// 表单配置
const options = computed(() => {
  return {
    formItems: [
      {
        type: 'input',
        prop: 'name',
        label: '规格名称',
        placeholder: '规格名称',
        rules: rules.name,
      },
      {
        type: 'input-number',
        prop: 'order',
        label: '排序',
        attrs: {
          min: 0,
          max: 1000,
        },
      },
      {
        type: 'switch',
        prop: 'status',
        label: '状态',
        attrs: {
          activeValue: 1,
          inactiveValue: 0,
        },
      },
      {
        type: 'slot',
        label: '规格值',
        prop: 'default',
        slotName: 'skuValue',
        // 规则不可用
        isHiddenObj: {
          status: 1,
        },
        rules: [
          {
            required: true,
            message: '规格值不能为空',
            trigger: 'change'
          },
        ],
      },
    ],
  }
})

</script>
